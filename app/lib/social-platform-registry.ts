/**
 * Normalize a raw platform string for alias-map lookup.
 * Lowercases, trims, and removes all spaces, underscores, and hyphens.
 */
export function normalizePlatformKey(raw: string): string {
    return raw.toLowerCase().trim().replace(/[\s_-]+/g, "");
}

// Map of normalized key/alias → registry entry. Populated once on module load.
const ALIAS_MAP = new Map<string, SocialPlatformEntry>();

for (const entry of SOCIAL_PLATFORM_REGISTRY) {
    ALIAS_MAP.set(entry.key, entry);
    for (const alias of entry.aliases) {
        ALIAS_MAP.set(normalizePlatformKey(alias), entry);
    }
}

// =============================================================================
// PUBLIC HELPERS
// =============================================================================

/**
 * Resolve a raw platform name (from Shopify or any source) to its registry entry.
 * Returns undefined when the platform is not registered — callers should fall back
 * to a globe/link icon.
 */
export function resolvePlatform(raw: string): SocialPlatformEntry | undefined {
    return ALIAS_MAP.get(normalizePlatformKey(raw));
}

/**
 * Return the display label for a platform, or the raw string if unrecognized.
 */
export function getSocialLabel(raw: string): string {
    return resolvePlatform(raw)?.label ?? raw;
}
